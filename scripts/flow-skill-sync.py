#!/usr/bin/env python3
"""flow-skill-sync.py - Regenerate the global flow-* skill mirror from repo commands.

Part of Claude Power Pack (CPP). Companion to scripts/skill-drift.py (which
detects *removal* drift against the deprecation list) - this script detects and
fixes *content* drift between the repo source of truth and the installed mirror.

Background
----------
CPP keeps two surfaces for the /flow workflow:

  * Source of truth : <repo>/.claude/commands/flow/<name>.md   (edited by PRs)
  * Global mirror   : ~/.claude/skills/flow-<name>/SKILL.md    (what EXECUTES
                       when a flow skill is invoked)

The manifest-driven generator that once produced the mirror was retired, so the
mirror became hand-maintained and silently drifted: PR #453 rebased the repo
commands onto native worktrees (EnterWorktree/ExitWorktree) but left the mirror
on the old manual `git worktree add` sibling-dir mechanics, still referencing a
retired Plane backend. This script closes that gap and guards against recurrence
(issue #457).

Each generated SKILL.md is: the CPP `GENERATED` marker, skill frontmatter
(name/description/argument-hint/metadata), then the VERBATIM body of the repo
command (its own leading frontmatter, if any, stripped). The frontmatter records
`source` (the repo command path) and `source_sha256` (a hash of the source file),
so drift is a one-line comparison: recompute the source hash and check it against
what the installed skill recorded.

Usage
-----
  flow-skill-sync.py                 # --check (report drift; exit 1 if any)
  flow-skill-sync.py --check         # explicit
  flow-skill-sync.py --write         # (re)generate the mirror from repo commands
  flow-skill-sync.py --json          # machine-readable drift findings
  flow-skill-sync.py --help

Options
  --skills-dir DIR    Mirror location (default: ~/.claude/skills)
  --commands-dir DIR  Source commands (default: <repo>/.claude/commands/flow)

Exit codes
  0 - No drift (--check), or write succeeded
  1 - Drift detected (--check/--json), or write failed
  2 - Usage / environment error
"""

from __future__ import annotations

import argparse
import hashlib
import json
import os
