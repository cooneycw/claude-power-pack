import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]

# Detection substring shared with scripts/skill-drift.py (is_generated). A single
# dash is used deliberately: this file is repo-tracked and CLAUDE.md forbids em
# dashes. The marker also names the real source so a maintainer who opens the
# generated file knows not to hand-edit it.
MARKER_PREFIX = "GENERATED by claude-power-pack"


def _marker(name: str) -> str:
    return (
        f"<!-- {MARKER_PREFIX} - scripts/flow-skill-sync.py; "
        f"edit .claude/commands/flow/{name}.md instead -->"
    )


