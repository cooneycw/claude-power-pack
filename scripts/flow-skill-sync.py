# Drift statuses
OK = "OK"
DRIFTED = "DRIFTED"
MISSING = "MISSING"
STALE_SOURCE = "STALE_SOURCE"  # source command file vanished from the repo


def _sha256(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def _strip_leading_frontmatter(text: str) -> str:
    """Drop a leading `---\\n ... \\n---\\n` YAML block if the command has one.

    The repo commands check/cleanup/doctor carry their own frontmatter
    (description + allowed-tools); the rest start straight at `# Flow: ...`. The
    skill supplies its own frontmatter, so the command's is redundant in the body.
    """
    if not text.startswith("---\n"):
        return text
    end = text.find("\n---\n", 4)
    if end == -1:
        return text
    return text[end + len("\n---\n") :].lstrip("\n")


def _render_skill(spec: dict, source_text: str, source_sha: str) -> str:
    name = f"flow-{spec['cmd']}"
    body = _strip_leading_frontmatter(source_text).rstrip("\n")
    lines = [
        _marker(spec["cmd"]),
        "---",
        f"name: {name}",
        f"description: {spec['description']}",
        f"argument-hint: {spec['argument_hint']}",
        "metadata:",
        f"  source: .claude/commands/flow/{spec['cmd']}.md",
        f"  source_sha256: {source_sha}",
        "  family: flow",
        f"  order: {spec['order']}",
        "---",
        "",
        body,
        "",
    ]
    return "\n".join(lines)


def _source_path(commands_dir: Path, cmd: str) -> Path:
    return commands_dir / f"{cmd}.md"


def _recorded_sha(skill_md: Path) -> str | None:
    """Return the source_sha256 recorded in an installed skill, or None."""
    try:
        for line in skill_md.read_text(encoding="utf-8", errors="replace").splitlines():
            m = re.match(r"\s*source_sha256:\s*([0-9a-f]{64})\s*$", line)
            if m:
                return m.group(1)
    except OSError:
        pass
    return None


def classify(skills_dir: Path, commands_dir: Path) -> list[dict]:
    findings: list[dict] = []
    for spec in SKILLS:
        cmd = spec["cmd"]
        name = f"flow-{cmd}"
        src = _source_path(commands_dir, cmd)
        if not src.is_file():
            findings.append({"skill": name, "status": STALE_SOURCE,
                             "detail": f"source {src} not found"})
            continue
        source_sha = _sha256(src.read_bytes())
        skill_md = skills_dir / name / "SKILL.md"
        if not skill_md.is_file():
            findings.append({"skill": name, "status": MISSING,
                             "detail": "not installed in mirror"})
            continue
        recorded = _recorded_sha(skill_md)
        if recorded == source_sha:
            findings.append({"skill": name, "status": OK, "detail": ""})
        else:
            why = "no source_sha256 recorded" if recorded is None else "source changed"
            findings.append({"skill": name, "status": DRIFTED, "detail": why})
    return findings


def drifted(findings: list[dict]) -> list[dict]:
    return [f for f in findings if f["status"] != OK]


def write_mirror(skills_dir: Path, commands_dir: Path) -> int:
    written = 0
    for spec in SKILLS:
        cmd = spec["cmd"]
        name = f"flow-{cmd}"
        src = _source_path(commands_dir, cmd)
        if not src.is_file():
            print(f"SKIP {name}: source {src} not found", file=sys.stderr)
            continue
        raw = src.read_bytes()
        content = _render_skill(spec, raw.decode("utf-8"), _sha256(raw))
        dest = skills_dir / name / "SKILL.md"
        dest.parent.mkdir(parents=True, exist_ok=True)
        dest.write_text(content, encoding="utf-8")
        print(f"WROTE {dest}")
        written += 1
    print(f"\n{written} flow skill(s) synced from {commands_dir} -> {skills_dir}")
    return 0 if written == len(SKILLS) else 1


def render_table(findings: list[dict]) -> str:
    lines = ["Flow Skill Mirror Drift", "=======================", ""]
    lines.append(f"{'Skill':<16} {'Status':<12} Detail")
    lines.append(f"{'-' * 16} {'-' * 12} {'-' * 30}")
    for f in findings:
        lines.append(f"{f['skill']:<16} {f['status']:<12} {f['detail']}")
    lines.append("")
    bad = drifted(findings)
    if bad:
        lines.append(
            f"{len(bad)} skill(s) out of sync with .claude/commands/flow/. "
            "Regenerate: python3 scripts/flow-skill-sync.py --write"
        )
    else:
        lines.append("Global flow-* mirror matches repo commands.")
    return "\n".join(lines)


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        add_help=True, description=__doc__,
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    mode = parser.add_mutually_exclusive_group()
    mode.add_argument("--check", action="store_true",
                      help="Report drift table (default); exit 1 if drift found")
    mode.add_argument("--write", action="store_true",
                      help="(Re)generate the global flow-* mirror from repo commands")
    parser.add_argument("--json", action="store_true", help="Emit findings as JSON")
    parser.add_argument("--skills-dir", default=None)
    parser.add_argument("--commands-dir", default=None)
    args = parser.parse_args(argv)

    home = Path(os.path.expanduser("~"))
    skills_dir = Path(args.skills_dir) if args.skills_dir else home / ".claude" / "skills"
    commands_dir = (
        Path(args.commands_dir) if args.commands_dir
        else REPO_ROOT / ".claude" / "commands" / "flow"
    )

    if not commands_dir.is_dir():
        print(f"ERROR: commands dir not found: {commands_dir}", file=sys.stderr)
        return 2

    if args.write:
        return write_mirror(skills_dir, commands_dir)

    findings = classify(skills_dir, commands_dir)
    if args.json:
        print(json.dumps(findings, indent=2))
        return 1 if drifted(findings) else 0

    print(render_table(findings))
    return 1 if drifted(findings) else 0


if __name__ == "__main__":
    sys.exit(main())
